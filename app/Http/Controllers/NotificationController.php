    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
{
    $user = auth()->user();
    return response()->json(Notification::where('hospital', $user->hospital)
        ->orWhere('current_hospital', $user->hospital)
        ->get());
}


    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $notify = new Notification;
        $notify->notification_text = $request->input('notification_text');
        $notify->hospital = $request->input('hospital');
        $notify->current_hospital = Auth::user()->hospital;

        $notify->save();
        
        return response()->json([
            'message' => 'User created successfully',
            'notificaton' => $notify,
        ], 201);
    }

